/// This is not the best way to do it of course, but it works, so I will simply keep this CGObject class, and then make it so we just go by the CGObject_C struct for all data.
/// </summary>
class CGObject {
public:
	CGObject_C* m_rawObject;

	// Constructor - takes raw pointer from game
	CGObject(void* rawPtr = nullptr) : m_rawObject(nullptr), m_objectData(nullptr)
	{
		if (rawPtr) {
			m_rawObject = static_cast<CGObject_C*>(rawPtr);
			if (m_rawObject) {
				m_objectData = m_rawObject->m_objectData;
			}
		}
	}

	// Check if valid
	bool IsValid() const {
		return m_rawObject != nullptr && m_objectData != nullptr;
	}

	// SIMPLE GETTERS - exactly what you asked for
	uint64_t GetGUID() const {
		return IsValid() ? m_objectData->m_guid : 0;
	}

	uint8_t GetType() const {
		return IsValid() ? m_objectData->m_type : 0;
	}

	int32_t GetEntryID() const {
		return IsValid() ? m_objectData->m_entryID : 0;
	}

	float GetScale() const {
		return IsValid() ? m_objectData->m_scale : 1.0f;
	}

	// Position getters (using MoveData from your struct)
	float GetX() const {
		return m_rawObject ? m_rawObject->MoveData_PosX : 0.0f;
	}

	float GetY() const {
		return m_rawObject ? m_rawObject->MoveData_PosY : 0.0f;
	}

	float GetZ() const {
		return m_rawObject ? m_rawObject->MoveData_PosZ : 0.0f;
	}

	float GetFacing() const {
		return m_rawObject ? m_rawObject->MoveData_Facing : 0.0f;
	}

	uint32_t GetHealth() const {
		return m_rawObject ? m_rawObject->UnitHealth : 0;
	}

	uint8_t GetClassID() const {
		return m_rawObject ? m_rawObject->unitBytes0.unitClass : 0;
	}

	// Raw pointer access
	void* GetRawPointer() const { return m_rawObject; }

private:
	CGObjectData* m_objectData;
};